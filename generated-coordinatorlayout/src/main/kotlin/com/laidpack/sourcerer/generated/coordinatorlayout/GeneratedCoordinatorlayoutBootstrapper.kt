package com.laidpack.sourcerer.generated.coordinatorlayout

import androidx.coordinatorlayout.widget.CoordinatorLayout
import com.laidpack.sourcerer.services.InflaterComponent
import com.laidpack.sourcerer.services.SerializerComponent
import com.laidpack.sourcerer.services.api.Bootstrapper
import com.squareup.moshi.Moshi

class GeneratedCoordinatorlayoutBootstrapper : Bootstrapper {
    override fun bootstrap(serializer: SerializerComponent, inflater: InflaterComponent) {
        serializer.registerAutoGeneratedAdapter(CoordinatorLayout::class, {moshi -> CoordinatorLayoutAttributesJsonAdapter(moshi as Moshi)}, "coordinatorLayout")
        serializer.registerAutoGeneratedAdapter(CoordinatorLayout.LayoutParams::class, {moshi -> CoordinatorLayoutLayoutParamsAttributesJsonAdapter(moshi as Moshi)}, "androidx.coordinatorlayout.widget.CoordinatorLayout.LayoutParams")
        serializer.associateViewGroupWithLayoutParams("coordinatorLayout", "androidx.coordinatorlayout.widget.CoordinatorLayout.LayoutParams")
        inflater.addFactory(CoordinatorLayoutFactory<CoordinatorLayout, CoordinatorLayoutAttributes>())
        inflater.addFactory(CoordinatorLayoutLayoutParamsFactory<CoordinatorLayout.LayoutParams, CoordinatorLayoutLayoutParamsAttributes>())
    }
}

package com.laidpack.sourcerer.generated.emoji

import androidx.emoji.widget.EmojiButton
import androidx.emoji.widget.EmojiEditText
import androidx.emoji.widget.EmojiExtractTextLayout
import androidx.emoji.widget.EmojiTextView
import com.laidpack.sourcerer.services.InflaterComponent
import com.laidpack.sourcerer.services.SerializerComponent
import com.laidpack.sourcerer.services.api.Bootstrapper
import com.squareup.moshi.Moshi

class GeneratedEmojiBootstrapper : Bootstrapper {
    override fun bootstrap(serializer: SerializerComponent, inflater: InflaterComponent) {
        serializer.registerAutoGeneratedAdapter(EmojiButton::class, {moshi -> EmojiButtonAttributesJsonAdapter(moshi as Moshi)}, "emojiButton")
        serializer.registerAutoGeneratedAdapter(EmojiEditText::class, {moshi -> EmojiEditTextAttributesJsonAdapter(moshi as Moshi)}, "emojiEditText")
        serializer.registerAutoGeneratedAdapter(EmojiTextView::class, {moshi -> EmojiTextViewAttributesJsonAdapter(moshi as Moshi)}, "emojiTextView")
        serializer.registerAutoGeneratedAdapter(EmojiExtractTextLayout::class, {moshi -> EmojiExtractTextLayoutAttributesJsonAdapter(moshi as Moshi)}, "emojiExtractTextLayout")
        serializer.associateViewGroupWithLayoutParams("emojiExtractTextLayout", "android.view.ViewGroup.LayoutParams")
        inflater.addFactory(EmojiButtonFactory<EmojiButton, EmojiButtonAttributes>())
        inflater.addFactory(EmojiEditTextFactory<EmojiEditText, EmojiEditTextAttributes>())
        inflater.addFactory(EmojiTextViewFactory<EmojiTextView, EmojiTextViewAttributes>())
        inflater.addFactory(EmojiExtractTextLayoutFactory<EmojiExtractTextLayout, EmojiExtractTextLayoutAttributes>())
    }
}

package com.laidpack.sourcerer.generated.drawerlayout

import androidx.drawerlayout.widget.DrawerLayout
import com.laidpack.sourcerer.services.InflaterComponent
import com.laidpack.sourcerer.services.SerializerComponent
import com.laidpack.sourcerer.services.api.Bootstrapper
import com.squareup.moshi.Moshi

class GeneratedDrawerlayoutBootstrapper : Bootstrapper {
    override fun bootstrap(serializer: SerializerComponent, inflater: InflaterComponent) {
        serializer.registerAutoGeneratedAdapter(DrawerLayout::class, {moshi -> DrawerLayoutAttributesJsonAdapter(moshi as Moshi)}, "drawerLayout")
        serializer.registerAutoGeneratedAdapter(DrawerLayout.LayoutParams::class, {moshi -> DrawerLayoutLayoutParamsAttributesJsonAdapter(moshi as Moshi)}, "androidx.drawerlayout.widget.DrawerLayout.LayoutParams")
        serializer.associateViewGroupWithLayoutParams("drawerLayout", "androidx.drawerlayout.widget.DrawerLayout.LayoutParams")
        inflater.addFactory(DrawerLayoutFactory<DrawerLayout, DrawerLayoutAttributes>())
        inflater.addFactory(DrawerLayoutLayoutParamsFactory<DrawerLayout.LayoutParams, DrawerLayoutLayoutParamsAttributes>())
    }
}

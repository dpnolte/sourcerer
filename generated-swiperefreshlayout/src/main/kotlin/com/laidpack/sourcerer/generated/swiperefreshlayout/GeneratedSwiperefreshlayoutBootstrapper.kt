package com.laidpack.sourcerer.generated.swiperefreshlayout

import androidx.swiperefreshlayout.widget.SwipeRefreshLayout
import com.laidpack.sourcerer.services.InflaterComponent
import com.laidpack.sourcerer.services.SerializerComponent
import com.laidpack.sourcerer.services.api.Bootstrapper
import com.squareup.moshi.Moshi

class GeneratedSwiperefreshlayoutBootstrapper : Bootstrapper {
    override fun bootstrap(serializer: SerializerComponent, inflater: InflaterComponent) {
        serializer.registerAutoGeneratedAdapter(SwipeRefreshLayout::class, {moshi -> SwipeRefreshLayoutAttributesJsonAdapter(moshi as Moshi)}, "swipeRefreshLayout")
        serializer.associateViewGroupWithLayoutParams("swipeRefreshLayout", "android.view.ViewGroup.LayoutParams")
        inflater.addFactory(SwipeRefreshLayoutFactory<SwipeRefreshLayout, SwipeRefreshLayoutAttributes>())
    }
}

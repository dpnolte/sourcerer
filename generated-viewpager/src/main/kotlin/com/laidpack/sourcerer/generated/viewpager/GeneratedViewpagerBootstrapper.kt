package com.laidpack.sourcerer.generated.viewpager

import androidx.viewpager.widget.PagerTabStrip
import androidx.viewpager.widget.PagerTitleStrip
import androidx.viewpager.widget.ViewPager
import com.laidpack.sourcerer.services.InflaterComponent
import com.laidpack.sourcerer.services.SerializerComponent
import com.laidpack.sourcerer.services.api.Bootstrapper
import com.squareup.moshi.Moshi

class GeneratedViewpagerBootstrapper : Bootstrapper {
    override fun bootstrap(serializer: SerializerComponent, inflater: InflaterComponent) {
        serializer.registerAutoGeneratedAdapter(ViewPager::class, {moshi -> ViewPagerAttributesJsonAdapter(moshi as Moshi)}, "viewPager")
        serializer.registerAutoGeneratedAdapter(ViewPager.LayoutParams::class, {moshi -> ViewPagerLayoutParamsAttributesJsonAdapter(moshi as Moshi)}, "androidx.viewpager.widget.ViewPager.LayoutParams")
        serializer.registerAutoGeneratedAdapter(PagerTitleStrip::class, {moshi -> PagerTitleStripAttributesJsonAdapter(moshi as Moshi)}, "pagerTitleStrip")
        serializer.registerAutoGeneratedAdapter(PagerTabStrip::class, {moshi -> PagerTabStripAttributesJsonAdapter(moshi as Moshi)}, "pagerTabStrip")
        serializer.associateThisViewGroupWithLayoutParams("viewPager", "androidx.viewpager.widget.ViewPager.LayoutParams")
        serializer.associateThisViewGroupWithLayoutParams("pagerTitleStrip", "android.view.ViewGroup.LayoutParams")
        serializer.associateThisViewGroupWithLayoutParams("pagerTabStrip", "android.view.ViewGroup.LayoutParams")
        inflater.addFactory(ViewPagerFactory<ViewPager, ViewPagerAttributes>())
        inflater.addFactory(ViewPagerLayoutParamsFactory<ViewPager.LayoutParams, ViewPagerLayoutParamsAttributes>())
        inflater.addFactory(PagerTitleStripFactory<PagerTitleStrip, PagerTitleStripAttributes>())
        inflater.addFactory(PagerTabStripFactory<PagerTabStrip, PagerTabStripAttributes>())
    }
}

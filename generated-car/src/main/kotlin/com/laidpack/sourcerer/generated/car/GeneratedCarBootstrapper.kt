package com.laidpack.sourcerer.generated.car

import androidx.car.widget.ActionBar
import androidx.car.widget.ColumnCardView
import androidx.car.widget.PagedListView
import androidx.car.widget.PagedScrollBarView
import com.laidpack.sourcerer.services.InflaterComponent
import com.laidpack.sourcerer.services.SerializerComponent
import com.laidpack.sourcerer.services.api.Bootstrapper
import com.squareup.moshi.Moshi

class GeneratedCarBootstrapper : Bootstrapper {
    override fun bootstrap(serializer: SerializerComponent, inflater: InflaterComponent) {
        serializer.registerAutoGeneratedAdapter(ColumnCardView::class, {moshi -> ColumnCardViewAttributesJsonAdapter(moshi as Moshi)}, "columnCardView")
        serializer.registerAutoGeneratedAdapter(PagedScrollBarView::class, {moshi -> PagedScrollBarViewAttributesJsonAdapter(moshi as Moshi)}, "pagedScrollBarView")
        serializer.registerAutoGeneratedAdapter(PagedListView::class, {moshi -> PagedListViewAttributesJsonAdapter(moshi as Moshi)}, "pagedListView")
        serializer.registerAutoGeneratedAdapter(ActionBar::class, {moshi -> ActionBarAttributesJsonAdapter(moshi as Moshi)}, "actionBar")
        serializer.associateViewGroupWithLayoutParams("columnCardView", "android.view.ViewGroup.LayoutParams")
        serializer.associateViewGroupWithLayoutParams("pagedScrollBarView", "android.view.ViewGroup.LayoutParams")
        serializer.associateViewGroupWithLayoutParams("pagedListView", "android.view.ViewGroup.LayoutParams")
        serializer.associateViewGroupWithLayoutParams("actionBar", "android.view.ViewGroup.LayoutParams")
        inflater.addFactory(ColumnCardViewFactory<ColumnCardViewAttributes>())
        inflater.addFactory(PagedScrollBarViewFactory<PagedScrollBarView, PagedScrollBarViewAttributes>())
        inflater.addFactory(PagedListViewFactory<PagedListView, PagedListViewAttributes>())
        inflater.addFactory(ActionBarFactory<ActionBar, ActionBarAttributes>())
    }
}

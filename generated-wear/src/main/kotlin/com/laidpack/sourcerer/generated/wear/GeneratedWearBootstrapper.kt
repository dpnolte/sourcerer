package com.laidpack.sourcerer.generated.wear

import androidx.wear.widget.BoxInsetLayout
import androidx.wear.widget.CircularProgressLayout
import androidx.wear.widget.SwipeDismissFrameLayout
import androidx.wear.widget.WearableRecyclerView
import androidx.wear.widget.drawer.WearableActionDrawerView
import androidx.wear.widget.drawer.WearableDrawerLayout
import androidx.wear.widget.drawer.WearableDrawerView
import androidx.wear.widget.drawer.WearableNavigationDrawerView
import com.laidpack.sourcerer.services.InflaterComponent
import com.laidpack.sourcerer.services.SerializerComponent
import com.laidpack.sourcerer.services.api.Bootstrapper
import com.squareup.moshi.Moshi

class GeneratedWearBootstrapper : Bootstrapper {
    override fun bootstrap(serializer: SerializerComponent, inflater: InflaterComponent) {
        serializer.registerAutoGeneratedAdapter(CircularProgressLayout::class, {moshi -> CircularProgressLayoutAttributesJsonAdapter(moshi as Moshi)}, "circularProgressLayout")
        serializer.registerAutoGeneratedAdapter(BoxInsetLayout::class, {moshi -> BoxInsetLayoutAttributesJsonAdapter(moshi as Moshi)}, "boxInsetLayout")
        serializer.registerAutoGeneratedAdapter(BoxInsetLayout.LayoutParams::class, {moshi -> BoxInsetLayoutLayoutParamsAttributesJsonAdapter(moshi as Moshi)}, "androidx.wear.widget.BoxInsetLayout.LayoutParams")
        serializer.registerAutoGeneratedAdapter(SwipeDismissFrameLayout::class, {moshi -> SwipeDismissFrameLayoutAttributesJsonAdapter(moshi as Moshi)}, "swipeDismissFrameLayout")
        serializer.registerAutoGeneratedAdapter(WearableDrawerView::class, {moshi -> WearableDrawerViewAttributesJsonAdapter(moshi as Moshi)}, "wearableDrawerView")
        serializer.registerAutoGeneratedAdapter(WearableNavigationDrawerView::class, {moshi -> WearableNavigationDrawerViewAttributesJsonAdapter(moshi as Moshi)}, "wearableNavigationDrawerView")
        serializer.registerAutoGeneratedAdapter(WearableActionDrawerView::class, {moshi -> WearableActionDrawerViewAttributesJsonAdapter(moshi as Moshi)}, "wearableActionDrawerView")
        serializer.registerAutoGeneratedAdapter(WearableDrawerLayout::class, {moshi -> WearableDrawerLayoutAttributesJsonAdapter(moshi as Moshi)}, "wearableDrawerLayout")
        serializer.registerAutoGeneratedAdapter(WearableRecyclerView::class, {moshi -> WearableRecyclerViewAttributesJsonAdapter(moshi as Moshi)}, "wearableRecyclerView")
        serializer.associateThisViewGroupWithLayoutParams("circularProgressLayout", "android.view.ViewGroup.LayoutParams")
        serializer.associateThisViewGroupWithLayoutParams("boxInsetLayout", "androidx.wear.widget.BoxInsetLayout.LayoutParams")
        serializer.associateThisViewGroupWithLayoutParams("swipeDismissFrameLayout", "android.view.ViewGroup.LayoutParams")
        serializer.associateThisViewGroupWithLayoutParams("wearableDrawerView", "android.view.ViewGroup.LayoutParams")
        serializer.associateThisViewGroupWithLayoutParams("wearableNavigationDrawerView", "android.view.ViewGroup.LayoutParams")
        serializer.associateThisViewGroupWithLayoutParams("wearableActionDrawerView", "android.view.ViewGroup.LayoutParams")
        serializer.associateThisViewGroupWithLayoutParams("wearableDrawerLayout", "android.view.ViewGroup.LayoutParams")
        serializer.associateThisViewGroupWithLayoutParams("wearableRecyclerView", "android.view.ViewGroup.LayoutParams")
        inflater.addFactory(CircularProgressLayoutFactory<CircularProgressLayout, CircularProgressLayoutAttributes>())
        inflater.addFactory(BoxInsetLayoutFactory<BoxInsetLayout, BoxInsetLayoutAttributes>())
        inflater.addFactory(BoxInsetLayoutLayoutParamsFactory<BoxInsetLayout.LayoutParams, BoxInsetLayoutLayoutParamsAttributes>())
        inflater.addFactory(SwipeDismissFrameLayoutFactory<SwipeDismissFrameLayout, SwipeDismissFrameLayoutAttributes>())
        inflater.addFactory(WearableDrawerViewFactory<WearableDrawerView, WearableDrawerViewAttributes>())
        inflater.addFactory(WearableNavigationDrawerViewFactory<WearableNavigationDrawerView, WearableNavigationDrawerViewAttributes>())
        inflater.addFactory(WearableActionDrawerViewFactory<WearableActionDrawerView, WearableActionDrawerViewAttributes>())
        inflater.addFactory(WearableDrawerLayoutFactory<WearableDrawerLayout, WearableDrawerLayoutAttributes>())
        inflater.addFactory(WearableRecyclerViewFactory<WearableRecyclerView, WearableRecyclerViewAttributes>())
    }
}

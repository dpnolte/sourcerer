package com.laidpack.sourcerer.generated.emoji.appcompat

import androidx.emoji.widget.EmojiAppCompatButton
import androidx.emoji.widget.EmojiAppCompatEditText
import androidx.emoji.widget.EmojiAppCompatTextView
import com.laidpack.sourcerer.services.InflaterComponent
import com.laidpack.sourcerer.services.SerializerComponent
import com.laidpack.sourcerer.services.api.Bootstrapper
import com.squareup.moshi.Moshi

class GeneratedEmojiAppcompatBootstrapper : Bootstrapper {
    override fun bootstrap(serializer: SerializerComponent, inflater: InflaterComponent) {
        serializer.registerAutoGeneratedAdapter(EmojiAppCompatEditText::class, {moshi -> EmojiAppCompatEditTextAttributesJsonAdapter(moshi as Moshi)}, "emojiAppCompatEditText")
        serializer.registerAutoGeneratedAdapter(EmojiAppCompatTextView::class, {moshi -> EmojiAppCompatTextViewAttributesJsonAdapter(moshi as Moshi)}, "emojiAppCompatTextView")
        serializer.registerAutoGeneratedAdapter(EmojiAppCompatButton::class, {moshi -> EmojiAppCompatButtonAttributesJsonAdapter(moshi as Moshi)}, "emojiAppCompatButton")
        inflater.addFactory(EmojiAppCompatEditTextFactory<EmojiAppCompatEditText, EmojiAppCompatEditTextAttributes>())
        inflater.addFactory(EmojiAppCompatTextViewFactory<EmojiAppCompatTextView, EmojiAppCompatTextViewAttributes>())
        inflater.addFactory(EmojiAppCompatButtonFactory<EmojiAppCompatButton, EmojiAppCompatButtonAttributes>())
    }
}

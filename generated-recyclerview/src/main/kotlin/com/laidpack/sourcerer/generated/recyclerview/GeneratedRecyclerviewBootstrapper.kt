package com.laidpack.sourcerer.generated.recyclerview

import androidx.recyclerview.widget.GridLayoutManager
import androidx.recyclerview.widget.RecyclerView
import androidx.recyclerview.widget.StaggeredGridLayoutManager
import com.laidpack.sourcerer.services.InflaterComponent
import com.laidpack.sourcerer.services.SerializerComponent
import com.laidpack.sourcerer.services.api.Bootstrapper
import com.squareup.moshi.Moshi

class GeneratedRecyclerviewBootstrapper : Bootstrapper {
    override fun bootstrap(serializer: SerializerComponent, inflater: InflaterComponent) {
        serializer.registerAutoGeneratedAdapter(RecyclerView::class, {moshi -> RecyclerViewAttributesJsonAdapter(moshi as Moshi)}, "recyclerView")
        serializer.registerAutoGeneratedAdapter(RecyclerView.LayoutParams::class, {moshi -> RecyclerViewLayoutParamsAttributesJsonAdapter(moshi as Moshi)}, "androidx.recyclerview.widget.RecyclerView.LayoutParams")
        serializer.registerAutoGeneratedAdapter(StaggeredGridLayoutManager.LayoutParams::class, {moshi -> StaggeredGridLayoutManagerLayoutParamsAttributesJsonAdapter(moshi as Moshi)}, "androidx.recyclerview.widget.StaggeredGridLayoutManager.LayoutParams")
        serializer.registerAutoGeneratedAdapter(GridLayoutManager.LayoutParams::class, {moshi -> GridLayoutManagerLayoutParamsAttributesJsonAdapter(moshi as Moshi)}, "androidx.recyclerview.widget.GridLayoutManager.LayoutParams")
        serializer.associateThisViewGroupWithLayoutParams("recyclerView", "androidx.recyclerview.widget.RecyclerView.LayoutParams")
        inflater.addFactory(RecyclerViewFactory<RecyclerView, RecyclerViewAttributes>())
        inflater.addFactory(RecyclerViewLayoutParamsFactory<RecyclerView.LayoutParams, RecyclerViewLayoutParamsAttributes>())
        inflater.addFactory(StaggeredGridLayoutManagerLayoutParamsFactory<StaggeredGridLayoutManager.LayoutParams, StaggeredGridLayoutManagerLayoutParamsAttributes>())
        inflater.addFactory(GridLayoutManagerLayoutParamsFactory<GridLayoutManager.LayoutParams, GridLayoutManagerLayoutParamsAttributes>())
    }
}

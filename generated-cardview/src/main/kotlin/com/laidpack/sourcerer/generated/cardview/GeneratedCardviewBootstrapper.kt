package com.laidpack.sourcerer.generated.cardview

import androidx.cardview.widget.CardView
import com.laidpack.sourcerer.services.InflaterComponent
import com.laidpack.sourcerer.services.SerializerComponent
import com.laidpack.sourcerer.services.api.Bootstrapper
import com.squareup.moshi.Moshi

class GeneratedCardviewBootstrapper : Bootstrapper {
    override fun bootstrap(serializer: SerializerComponent, inflater: InflaterComponent) {
        serializer.registerAutoGeneratedAdapter(CardView::class, {moshi -> CardViewAttributesJsonAdapter(moshi as Moshi)}, "cardView")
        serializer.associateViewGroupWithLayoutParams("cardView", "android.view.ViewGroup.LayoutParams")
        inflater.addFactory(CardViewFactory<CardView, CardViewAttributes>())
    }
}
